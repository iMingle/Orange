package org.mingle.orange.world;

public class Father {
	 private int age;

	public Father(int age) {
		super();
		this.age = age;
	}

	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}
	 
}
